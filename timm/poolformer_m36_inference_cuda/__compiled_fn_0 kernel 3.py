    x3 = xindex
    for roffset in range(0, rnumel, RBLOCK):
        rindex = roffset + rbase
        rmask = rindex < rnumel
        r2 = rindex
        tmp0 = r2 + (8137*x0)
        tmp1 = tl.full([1, 1], 301056, tl.int32)
        tmp2 = tmp0 < tmp1
        tmp3 = tl.load(in_ptr0 + ((301056*x1) + ((r2 + (8137*x0)) % 301056)), rmask & tmp2 & xmask, eviction_policy='evict_last', other=0.0)
